# Simple assignment operator
balance = 500

print(balance)

# Assignment operator with addition

balance = 500
balance += 200

print(balance)

# Assignment operator with subtraction

balance = 500
balance -= 100

print(balance)

# Assignment operator with multiplication

balance = 500
balance *= 2

print(balance)

# Assignment operator with division

balance = 500
balance /= 5

print(balance)

balance = 500
balance //= 5

print(balance)

# Assignment operator with modulus

balance = 500
balance %= 480

print(balance)

# Assignment operator with exponentiation

balance = 80
balance **= 2

print(balance)